def posts_list(request):
    query = request.GET.get('q')
    if query:
        posts = Post.objects.filter(title__icontains=query)  # <-- This is the check
    else:
        posts = Post.objects.all()

    return render(request, 'blog/posts.html', {'posts': posts})








        
